package team3647.lib.inputs;

public class ButtonPress implements Comparable<ButtonPress> {
    public final String name;
    public final double timestamp;

    public ButtonPress(String name, double timestamp){
        this.name = name;
        this.timestamp = timestamp;
    }

    @Override
    public int compareTo(ButtonPress o) {
        return Double.compare(this.timestamp, o.timestamp);
    }
}
